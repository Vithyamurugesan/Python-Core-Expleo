#Example for Type 

#int
num1=10
print(type(num1))
num2=-123
print(type(num2))

#boolean
var1=True
print(type(var1))

#float
num3=-123.5
print(type(num3))

#complex
com=4j+1
print(type(com))

#string
name="Vithya"
print(type(name))

#list
list1=[5,4,3,"new delhi",True]
print(type(list1));
print(type(list1[4])); #Checking the type using index

#tuple
tuple1=(10,11,"apple")
print(type(tuple1))
print(type(tuple1[2]))

#set (No indexing in set)
set1={1,2.3,"num1",True}
print(type(set1));

#none
myVar=None
print(type(myVar))

#dictionary
student={"name":"vithya","rollno":54}
print(type(student))
print(student['name'])






